namespace Orm.Toolkit.Telerik.Windows.Controls.Scheduler
{
    /// <summary>
    /// Determines the types of recurrences range. 
    /// </summary>
    public enum RecurrenceRangeType
    {
        /// <summary>
        /// Repeat continiously.
        /// </summary>
        NoEndDate,

        /// <summary>
        /// Number of times to repeat.
        /// </summary>
        MaxOccurrences,

        /// <summary>
        /// Repeat until a date.
        /// </summary>
        RepeatUntil
    }
}